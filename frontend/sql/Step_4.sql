-- КРОК 4: Підрахунок рядків у діапазоні дат
-- ===================================================

USE goit_rdb_hw04;

SELECT
    COUNT(*) AS orders_in_range
FROM orders
WHERE date BETWEEN '1996-07-10 00:00:00' AND '1996-10-08 00:00:00';

/*
🕰️ Інженерна нотатка:
Для виконання ДЗ використано BETWEEN. Проте у High-Load системах для DATETIME
часто безпечніше використовувати строгі нерівності (SARGable підхід):
WHERE date >= '1996-07-10 00:00:00' AND date < '1996-10-09 00:00:00'
Це гарантує захоплення всього дня 8 жовтня до 23:59:59.
*/
